import json
import datetime
import os
from reportlab.lib.pagesizes import letter
from reportlab.pdfgen import canvas
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle

class DillyReportGenerator:
    def __init__(self, audit_data):
        self.data = audit_data
        self.filename = f"dilly_report_{self.data['candidate']}.pdf"

    def generate(self):
        doc = SimpleDocTemplate(self.filename, pagesize=letter)
        styles = getSampleStyleSheet()
        story = []

        # Header
        title = Paragraph(f"<font size=24 color='#2E5BFF'>DILLY AUDIT REPORT</font>", styles['Title'])
        story.append(title)
        story.append(Spacer(1, 12))

        # Candidate Info
        story.append(Paragraph(f"<b>Candidate:</b> {self.data['candidate']}", styles['Normal']))
        story.append(Paragraph(f"<b>Date:</b> {self.data['timestamp']}", styles['Normal']))
        story.append(Paragraph(f"<b>Status:</b> <font color={'green' if self.data['veracity_status'] == 'VERIFIED' else 'red'}>{self.data['veracity_status']}</font>", styles['Normal']))
        story.append(Spacer(1, 24))

        # Score Section
        score_val = self.data['dilly_grit_score']
        score_color = 'green' if score_val > 70 else 'orange' if score_val > 40 else 'red'
        story.append(Paragraph(f"<font size=18>Dilly Grit Score: <font color='{score_color}'>{score_val}/100</font></font>", styles['Heading2']))
        story.append(Spacer(1, 12))

        # Metadata Table
        meta_data = [["Metric", "Value"]]
        for k, v in self.data['metadata'].items():
            meta_data.append([k.replace('_', ' ').title(), str(v)])
        
        t = Table(meta_data, colWidths=[200, 200])
        t.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.lightgrey),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.black),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('GRID', (0, 0), (-1, -1), 0.5, colors.grey)
        ]))
        story.append(t)
        story.append(Spacer(1, 24))

        # Footer
        footer_text = "<i>This report was generated by the Dilly Engine. Veracity is determined by cross-referencing resume claims against live repository activity and commit velocity.</i>"
        story.append(Paragraph(footer_text, styles['Italic']))

        doc.build(story)
        print(f"Report generated: {self.filename}")
        return self.filename

