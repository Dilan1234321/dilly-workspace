import requests

class DillyGitHubAuditor:
    def __init__(self, username):
        self.username = username
        self.base_url = "https://api.github.com"
        self.token = os.getenv("GITHUB_TOKEN") 
        self.headers = {"Authorization": f"token {self.token}"} if self.token else {}

    def fetch_user_data(self):
        response = requests.get(f"{self.base_url}/users/{self.username}", headers=self.headers)
        return response.json() if response.status_code == 200 else None

    def fetch_repos(self):
        response = requests.get(f"{self.base_url}/users/{self.username}/repos?sort=updated&per_page=100", headers=self.headers)
        return response.json() if response.status_code == 200 else []

    def calculate_grit_score(self, user_data, repos):
        if not user_data: return 0
        total_repos = user_data.get('public_repos', 0)
        if total_repos == 0: return 0
        now = datetime.datetime.now()
        active_repos = sum(1 for r in repos if (now - datetime.datetime.strptime(r['updated_at'], "%Y-%m-%dT%H:%M:%SZ")).days < 30)
        velocity = (active_repos / total_repos) * 100
        score = (total_repos * 2) + (velocity * 0.8)
        return min(round(score, 2), 100)

    def run_audit(self):
        user = self.fetch_user_data()
        repos = self.fetch_repos()
        score = self.calculate_grit_score(user, repos)
        
        report_data = {
            "candidate": self.username,
            "dilly_grit_score": score,
            "timestamp": datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            "veracity_status": "VERIFIED" if score > 50 else "PENDING",
            "metadata": {
                "public_repos": user.get('public_repos', 0) if user else 0,
                "active_projects_30d": sum(1 for r in repos if (datetime.datetime.now() - datetime.datetime.strptime(r['updated_at'], "%Y-%m-%dT%H:%M:%SZ")).days < 30),
                "account_age_days": (datetime.datetime.now() - datetime.datetime.strptime(user['created_at'], "%Y-%m-%dT%H:%M:%SZ")).days if user else 0
            }
        }
        
        gen = DillyReportGenerator(report_data)
        gen.generate()
        return report_data

