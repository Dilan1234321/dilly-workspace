export function isSupportedLang(code: string | null | undefined): code is LangCode {
  return !!code && (SUPPORTED_LANGS.map((l) => l.code) as string[]).includes(code);
}

export function normalizeLang(code: string | null | undefined): LangCode {
  if (!code) return DEFAULT_LANG;
  const short = code.trim().toLowerCase().split("-")[0];
  return isSupportedLang(short) ? (short as LangCode) : DEFAULT_LANG;
}

export function t(lang: LangCode, key: string, vars?: Record<string, string | number>): string {
  const dict = T[lang] ?? T[DEFAULT_LANG];
  let s = dict[key] ?? T[DEFAULT_LANG][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}
